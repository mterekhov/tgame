#include "APoint.h"
#include "DrawTypes.h"

//==============================================================================

APoint::APoint() : x(0.0f), y(0.0f), z(0.0f)
{
}

//==============================================================================

APoint::APoint(const GLfloat x, const GLfloat y, const GLfloat z) : x(x), y(y), z(z)
{
}

//==============================================================================

APoint::APoint(const APoint& point)
{
    x = point.x;
    y = point.y;
    z = point.z;
}

//==============================================================================

APoint::~APoint()
{
}
