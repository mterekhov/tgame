#include "apoint2d.h"

//==============================================================================

namespace spcTGame
{
    
//==============================================================================
    
APoint2D::APoint2D() : x(0.0f), y(0.0f)
{
}

//==============================================================================

APoint2D::APoint2D(const TFloat x, const TFloat y) : x(x), y(y)
{
}

//==============================================================================

APoint2D::APoint2D(const APoint2D& point)
{
    x = point.x;
    y = point.y;
}

//==============================================================================

APoint2D::~APoint2D()
{
}

//==============================================================================
    
}   //  namespace spcTGame
