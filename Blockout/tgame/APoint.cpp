#include "apoint.h"

//==============================================================================

namespace spcTGame
{
    
//==============================================================================
    
APoint::APoint() : x(0.0f), y(0.0f), z(0.0f)
{
}

//==============================================================================

APoint::APoint(const TFloat x, const TFloat y, const TFloat z) : x(x), y(y), z(z)
{
}

//==============================================================================

APoint::APoint(const APoint& point)
{
    x = point.x;
    y = point.y;
    z = point.z;
}

//==============================================================================

APoint::~APoint()
{
}

//==============================================================================
    
}   //  namespace spcTGame
